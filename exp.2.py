# Define a list
my_list = [1, 2, 3, 4, 5]

# Iterate over the list using a for loop
for item in my_list:
    print(item)
# Define a dictionary
my_dict = {'name': 'John', 'age': 30, 'city': 'New York'}

# Iterate over the dictionary using a for loop
for key, value in my_dict.items():
    print(f'{key}: {value}')
